public class OptionLab {

    public void basicSwitchExample(int day) {

        switch(day){

            case 1:

                System.out.println("day = 1");

                break;

            case 2:

                System.out.println("day = 2");

                break;

            case 3:

            case 4:

                System.out.println("day = 3 or 4");

                break;

            default:

                System.out.println("day is greater than 4.");

                break;

        }



    } // end method
}
